def buildAllPeople() -> List[Person]:
    return (
        buildPage0People()
        + buildPage1People()
        + buildPage2People()
        + buildPage3People()
    )


def writeJson(people: List[Person], outFile: Path) -> None:
    payload = {
        "source": "样章.json",
        "totalPages": 4,
        "totalCount": len(people),
        "fieldsExplain": {
            "name": "姓名",
            "generation": "世系",
            "birth_date": "出生年月（取自 OCR 原文，未载则空）",
            "spouse": "配偶（'X氏' 或具体名字；未载则空）",
            "father": "父亲（同一份 4 页家谱中找不到则空）",
            "page": "0~3 对应书页 三〇六~三〇九",
            "role": "main = 有独立条目；child = 仅作为子嗣登记",
            "note": "补充说明",
            "source_fragment": "来自 样章.json 的 OCR 原句",
        },
        "people": [asdict(p) for p in people],
    }
    outFile.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8"
    )


def writeCsv(people: List[Person], outFile: Path) -> None:
    fieldnames = [
        "name", "generation", "birth_date", "spouse", "father",
        "page", "role", "note", "source_fragment",
    ]
    with outFile.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for p in people:
            writer.writerow(asdict(p))


def main() -> None:
    here = Path(__file__).resolve().parent
    people = buildAllPeople()

    jsonOut = here / "genealogy_all_pages.json"
    csvOut = here / "genealogy_all_pages.csv"

    writeJson(people, jsonOut)
    writeCsv(people, csvOut)

    perPage = {p.page: 0 for p in people}
    for p in people:
        perPage[p.page] += 1

    print(f"[OK] {len(people)} people exported")
    for pg in sorted(perPage):
        print(f"     page {pg} ({['三〇六','三〇七','三〇八','三〇九'][pg]}): "
              f"{perPage[pg]} people")
    print(f"     -> {jsonOut}")
    print(f"     -> {csvOut}")


if __name__ == "__main__":
    main()
